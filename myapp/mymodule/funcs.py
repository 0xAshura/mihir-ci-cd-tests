"""
funcs.py contains four math functions.
"""


def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a * 1.0 / b

def calculate_square_perimeter(side_length):
    return 4 * side_length

def calculate_square_area(side_length):
    return side_length * side_length